function existingWorkspaceContext(targetDir) {
  const inspection = inspectWorkspace(targetDir);
  if (!inspection.ok) {
    return {
      affectedAreas: ['需要先读取当前项目，再定位与用户描述直接相关的页面和代码。'],
      protectionItems: ['不要修改与本次描述无关的现有功能。'],
      unknowns: ['当前项目无法完成只读扫描，需要先确认目录访问是否正常。'],
      verificationItems: ['修复后运行项目已有测试和构建检查。']
    };
  }

  const project = inspection.result.project;
  const facts = [
    project.platforms.length > 0 ? `项目类型：${project.platforms.join('、')}` : '',
    project.languages.length > 0 ? `语言：${project.languages.join('、')}` : '',
    project.buildSystems.length > 0 ? `构建方式：${project.buildSystems.join('、')}` : ''
  ].filter(Boolean);
  const affectedAreas = [
    facts.length > 0
      ? `当前只读扫描识别到${facts.join('；')}。`
      : '当前扫描没有足够证据指向具体技术区域。',
    '具体文件必须在读取源码和现有资料后按用户描述定位，不能凭问答猜测。'
  ];
  const protectionItems = [
    '保持与本次描述无关的现有功能不变。',
    ...(project.hasTests ? ['保留并运行当前项目已有测试。'] : []),
    ...(project.buildSystems.length > 0 ? [`保持现有 ${project.buildSystems.join('、')} 构建方式可用。`] : []),
    '不覆盖已有产品资料和用户维护的文档。'
  ];
  const unknowns = [
    '与这次改动直接相关的页面、代码入口和业务规则，需要先读取当前项目后确认。'
  ];
  const verificationItems = [
    project.hasTests ? '运行现有测试，并为这次改动补充最小回归验证。' : '先确认项目当前使用的测试方式，再补充最小回归验证。',
    project.buildSystems.length > 0
      ? `运行现有 ${project.buildSystems.join('、')} 构建检查。`
      : '确认项目的构建方式并完成一次本地构建。',
    '发布前确认没有修改无关功能、用户数据或权限边界。'
  ];
  return { affectedAreas, protectionItems, unknowns, verificationItems };
}

