function questionsNeedingConfirmation(answers) {
  const questions = [];
  if (!hasValue(answers.features)) {
    questions.push('还没有说清楚希望它能做哪些事。');
  }
  if (!hasValue(answers.platform)) {
    questions.push('使用方式还没有选择。');
  } else if (answers.platform === '还没想好') {
    questions.push('使用方式暂未确定，创建源码工程前需要再确认。');
  }
  return questions.length > 0 ? questions : ['暂时没有会阻止第一步开发的其他问题。'];
}

function valueOrPending(value) {
  return hasValue(value) ? value.trim() : '待确认';
}

function platformUsageDescription(value) {
  if (value === 'iPhone') return '先在 iPhone 上使用';
  if (value === 'Mac') return '先在 Mac 上使用';
  if (value === '网页') return '先通过网页使用';
  return '使用方式暂未确定';
}

function markdownCell(value) {
  return value.replaceAll('|', '\\|').replaceAll('\n', '<br>');
}

function focusedIntakeFrom(answers) {
  if (answers?.newProject && typeof answers.newProject === 'object') {
    return { mode: 'new_project', answers: normalizeNewProjectAnswers(answers.newProject) };
  }
  if (answers?.existingProject && typeof answers.existingProject === 'object') {
    return { mode: 'existing_project', answers: answers.existingProject };
  }
  return null;
}

function normalizeNewProjectAnswers(answers) {
  const idea = stringValue(answers.idea) || stringValue(answers.projectSummary);
  const features = stringValue(answers.features)
    || listOrTextValue(answers.coreFeatures)
    || stringValue(answers.firstVersion)
    || stringValue(answers.primaryGoal);
  const restoredPlatform = stringValue(answers.platform) || stringValue(answers.targetPlatform);
  const platform = ['iPhone', 'Mac', '网页', '还没想好'].includes(restoredPlatform)
    ? restoredPlatform
    : '';
  return { idea, features, firstVersion: features, platform };
}

function stringValue(value) {
  return typeof value === 'string' ? value.trim() : '';
}

function listOrTextValue(value) {
  if (Array.isArray(value)) return value.filter((item) => hasValue(item)).join('、');
  return stringValue(value);
}

function filledCoreFiles(targetDir) {
  return coreFiles.filter((file) => {
    const fullPath = path.join(targetDir, file);
    if (!fs.existsSync(fullPath)) return false;
    const text = fs.readFileSync(fullPath, 'utf8');
    return validateCoreFile(file, text).length === 0;
  });
}

function renderCoreFiles(answers) {
  return {
    'product/product-brief.zh-CN.md': renderProductBrief(answers),
    'product/scope-gate.zh-CN.md': renderScopeGate(answers),
    'product/screen-states.zh-CN.md': renderScreenStates(answers),
    'product/data-safety.zh-CN.md': renderDataSafety(answers)
  };
}

function renderProductBrief(answers) {
  const lines = [
    '# 产品简报',
    '',
    `主要用户：${answers.primaryUser}`,
    `第一版先证明一件事：${answers.firstVersionGoal}`,
    `用户能完成：${answers.userCanDo}`
  ];

  if (hasValue(answers.successEvidence)) lines.push(`我们能看到的证据：${answers.successEvidence}`);
  if (hasValue(answers.cannotCollect)) lines.push(`不能收集：${answers.cannotCollect}`);
  if (hasItems(answers.humanConfirmActions)) {
    lines.push(`会影响真实用户或钱的动作：${listSentence(answers.humanConfirmActions)}`);
  }
  return lines.join('\n') + '\n';
}

function renderScopeGate(answers) {
  const lines = [
    '# 范围门禁',
    '',
    '## 首版必须做',
    bulletList(answers.mustDo),
    '',
    '## 首版明确不做',
    bulletList(answers.wontDo)
  ];

  if (hasItems(answers.aiMustNotAdd)) {
    lines.push('', '## 不要让 AI 自己加', bulletList(answers.aiMustNotAdd));
  }
  if (hasItems(answers.humanConfirmActions)) {
    lines.push('', '## 需要人工确认的动作', bulletList(answers.humanConfirmActions));
  }
  return lines.join('\n') + '\n';
}

function renderScreenStates(answers) {
  const screen = answers.mainScreen ?? {};
  const fields = [
    ['页面', screen.name],
    ['用户想做什么', screen.userGoal],
    ['加载中', screen.loading],
    ['空状态', screen.empty],
    ['错误状态', screen.error],
    ['成功状态', screen.success],
    ['权限拒绝', screen.permissionDenied]
  ].filter(([, value]) => hasValue(value));
  return [
    '# 页面状态',
    '',
    `| ${fields.map(([title]) => title).join(' | ')} |`,
    `|${fields.map(() => '---').join('|')}|`,
    `| ${fields.map(([, value]) => value).join(' | ')} |`
  ].join('\n') + '\n';
}

function renderDataSafety(answers) {
  const data = answers.dataSafety ?? {};
  const lines = [
    '# 数据安全',
    ''
  ];
  for (const value of [data.collects, data.thirdParties, data.doesNotCollect, data.deletion, data.retention]) {
    if (hasValue(value)) lines.push(`${value}。`);
  }
  return lines.join('\n') + '\n';
}

function splitList(value) {
  return value
    .split(/[，,、]/)
    .map((item) => item.trim())
    .filter(Boolean);
}

function setAnswerAtPath(answers, answerPath, value) {
  const parts = answerPath.split('.');
  const key = parts.pop();
  const target = parts.reduce((current, part) => current[part] ||= {}, answers);
  target[key] = value;
}

function bulletList(items) {
  return (items ?? []).map((item) => `- ${item}`).join('\n');
}

function listSentence(items) {
  return (items ?? []).join('、');
}

function hasValue(value) {
  return typeof value === 'string' && value.trim().length > 0;
}

function hasItems(items) {
  return Array.isArray(items) && items.length > 0;
}
